#include <stdio.h>

/**
 * add - function add integers
 * @a: param
 * @b: param
 *
 * Return: 0
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - function subtract integers
 * @a: param
 * @b: param
 *
 * Return: 0
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - function multiply integers
 * @a: param
 * @b: param
 *
 * Return: 0
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - function divide integers
 * @a: param
 * @b: param
 *
 * Return: 0
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - function remainder integers
 * @a: param
 * @b: param
 *
 * Return: 0
 */
int mod(int a, int b)
{
	return (a % b);
}
